//Orden de las operaciones en JS

let resultado;
let resultado2;

resultado = (20 + 30) * 2;

resultado = (100 + 200 + 300) * 0.2;
resultado2 = (100 + 200 + 300) * 1.2;

console.log(resultado);
console.log(resultado2);

//Incrementos

let puntaje = 10;

//Incremento en 1
console.log(puntaje++); //10
console.log(puntaje); //11
console.log(++puntaje); //12

//Decremento en 1
puntaje--;
console.log(puntaje);

puntaje += 1;
console.log(puntaje);
